#!/usr/bin/env python3
"""ReGVD Devign robustness eval — all 7 obfuscation conditions.

Uses best.pt (single checkpoint, F1=62.45%) from regvd_devign/best.pt.
Pairwise JSON files generated by devign_full/gen_pairwise_obf_files.py.
"""
import json
from pathlib import Path

import torch
from torch_geometric.loader import DataLoader
from transformers import RobertaTokenizer, RobertaModel
from sklearn.metrics import f1_score

from train_regvd import (
    ReGVDDataset, ReGVDModel,
    HIDDEN_DIM, NUM_GNN_LAYERS, WINDOW_SIZE, BATCH_SIZE,
    SCRIPT_DIR, THESIS_ROOT, DEVIGN_ROOT, SPLIT_FILE,
    MODEL_DIR, CODEBERT_MODEL,
)

DEVIGN_ROOT = THESIS_ROOT / "devign_full"

ALL_DATA_FILES = {
    "test":                 DEVIGN_ROOT / "originals_full_data_with_slices.json",
    "test_obf_identifier":  DEVIGN_ROOT / "obf_identifier_full_data_with_slices.json",
    "test_obf_deadcode":    DEVIGN_ROOT / "obf_deadcode_full_data_with_slices.json",
    "test_obf_controlflow": DEVIGN_ROOT / "obf_controlflow_full_data_with_slices.json",
    "test_obf_ren_dead":    DEVIGN_ROOT / "obf_ren_dead_full_data_with_slices.json",
    "test_obf_ren_cf":      DEVIGN_ROOT / "obf_ren_cf_full_data_with_slices.json",
    "test_obf_dead_cf":     DEVIGN_ROOT / "obf_dead_cf_full_data_with_slices.json",
    "test_obf_compound":    DEVIGN_ROOT / "obf_compound_full_data_with_slices.json",
}


@torch.no_grad()
def evaluate(model, loader, device):
    model.eval()
    all_preds, all_labels = [], []
    for data in loader:
        data = data.to(device)
        preds = model(data).argmax(dim=-1).cpu().numpy()
        all_preds.extend(preds)
        all_labels.extend(data.y.cpu().numpy())
    return f1_score(all_labels, all_preds, zero_division=0) * 100


def main():
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
    print(f"Device: {device}", flush=True)

    split = json.load(open(SPLIT_FILE))
    test_files = set(split["splits"]["test"])

    print("Loading CodeBERT embeddings...", flush=True)
    tokenizer = RobertaTokenizer.from_pretrained(CODEBERT_MODEL)
    codebert  = RobertaModel.from_pretrained(CODEBERT_MODEL)
    embed_weight = codebert.embeddings.word_embeddings.weight.detach().cpu()
    del codebert

    model = ReGVDModel(in_dim=768, hidden_dim=HIDDEN_DIM,
                       num_layers=NUM_GNN_LAYERS).to(device)
    ckpt = MODEL_DIR / "best.pt"
    model.load_state_dict(torch.load(ckpt, map_location=device))
    model.eval()
    print("Model loaded.", flush=True)

    results = {}
    for cond, data_file in ALL_DATA_FILES.items():
        data_json = json.load(open(data_file))
        fn_map    = {r["file_name"]: r for r in data_json}
        recs      = [fn_map[f] for f in test_files if f in fn_map]
        ds        = ReGVDDataset(recs, embed_weight, tokenizer)
        loader    = DataLoader(ds, batch_size=BATCH_SIZE * 2, shuffle=False)
        f1        = evaluate(model, loader, device)
        results[cond] = round(f1, 2)
        print(f"  {cond:<30} F1={f1:.2f}%", flush=True)

    base = results["test"]
    print(f"\n=== RESULTS (single checkpoint, clean F1={base:.2f}%) ===")
    out = {"test": {"f1_mean": base, "f1_std": 0.0}}
    for cond, f1 in results.items():
        if cond == "test":
            continue
        delta = round(f1 - base, 2)
        out[cond] = {"f1_mean": f1, "f1_std": 0.0, "delta_f1": delta}
        sign = "+" if delta >= 0 else ""
        print(f"  {cond:<30} {f1:>6.2f}%  Δ={sign}{delta:.2f}pp")

    max_drop = max(abs(v["delta_f1"]) for v in out.values() if "delta_f1" in v)
    out["max_drop"] = round(max_drop, 2)
    out["model"]   = "ReGVD"
    out["dataset"] = "Devign"
    out["note"]    = "single checkpoint (best.pt)"

    out_path = THESIS_ROOT / "devign_full" / "regvd_7cond_devign_results.json"
    with open(out_path, "w") as f:
        json.dump(out, f, indent=2)
    print(f"\nSaved → {out_path}", flush=True)


if __name__ == "__main__":
    main()
